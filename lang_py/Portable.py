class Portable:

    def __init__(self, kodePortable, beratKG):
        self.kodePortable = kodePortable
        self.beratKG = beratKG

    def setKodePortable(self, kodePortable):
        self.kodePortable = kodePortable

    def getKodePortable(self):
        return self.kodePortable
    
    def setBeratKG(self, beratKG):
        self.beratKG = beratKG

    def getBeratKG(self):
        return self.beratKG