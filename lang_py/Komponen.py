class Komponen:
    
    def __init__(self, merk, nama):
        self.merk = merk
        self.nama = nama
        
    def setMerk(self, merk):
        self.merk = merk

    def getMerk(self):
        return self.merk
    
    def setNama(self, nama):
        self.nama = nama

    def getNama(self):
        return self.nama
